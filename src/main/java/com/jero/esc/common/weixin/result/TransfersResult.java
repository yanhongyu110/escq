public class TransfersResult {
	/** 返回状态码 */
	private String return_code;
	/** 返回信息 */
	private String return_msg;
	
	//以下在return_code为SUCCESS时有返回
	/** 业务结果 */
	private String result_code;
	/** 错误代码 */
	private String err_code;
	/** 错误代码描述 */
	private String err_code_des;
	/** 商户单号 */
	private String partner_trade_no;
	/** 商户号 */
	private String mch_id;
	/** 付款单号*/
	private String detail_id;
	/** 转账状态 */
	private String status;
	/** 收款用户openid*/
	private String reason;
	/** 返回状态码 */
	private String openid;
	/** 收款用户姓名 */
	private String transfer_name;
	/** 付款金额 */
	private String payment_amount;
	/** 转账时间 */
	private String transfer_time;
	/** 付款描述 */
	private String desc;
	
	public TransfersResult() {
		super();
	}
	
	public TransfersResult(String return_code, String return_msg, String result_code, String err_code, String err_code_des,
			String partner_trade_no, String mch_id, String detail_id, String status, String reason, String openid,
			String transfer_name, String payment_amount, String transfer_time, String desc) {
		super();
		this.return_code = return_code;
		this.return_msg = return_msg;
		this.result_code = result_code;
		this.err_code = err_code;
		this.err_code_des = err_code_des;
		this.partner_trade_no = partner_trade_no;
		this.mch_id = mch_id;
		this.detail_id = detail_id;
		this.status = status;
		this.reason = reason;
		this.openid = openid;
		this.transfer_name = transfer_name;
		this.payment_amount = payment_amount;
		this.transfer_time = transfer_time;
		this.desc = desc;
	}
	
	/**  
	 * 获取 return_code  
	 * @return return_code return_code  
	 */
	public String getReturn_code() {
		return return_code;
	}
	/**  
	 * 设置 return_code  
	 * @param return_code return_code  
	 */
	public void setReturn_code(String return_code) {
		this.return_code = return_code;
	}
	/**  
	 * 获取 return_msg  
	 * @return return_msg return_msg  
	 */
	public String getReturn_msg() {
		return return_msg;
	}
	/**  
	 * 设置 return_msg  
	 * @param return_msg return_msg  
	 */
	public void setReturn_msg(String return_msg) {
		this.return_msg = return_msg;
	}
	/**  
	 * 获取 result_code  
	 * @return result_code result_code  
	 */
	public String getResult_code() {
		return result_code;
	}
	/**  
	 * 设置 result_code  
	 * @param result_code result_code  
	 */
	public void setResult_code(String result_code) {
		this.result_code = result_code;
	}
	/**  
	 * 获取 err_code  
	 * @return err_code err_code  
	 */
	public String getErr_code() {
		return err_code;
	}
	/**  
	 * 设置 err_code  
	 * @param err_code err_code  
	 */
	public void setErr_code(String err_code) {
		this.err_code = err_code;
	}
	/**  
	 * 获取 err_code_des  
	 * @return err_code_des err_code_des  
	 */
	public String getErr_code_des() {
		return err_code_des;
	}
	/**  
	 * 设置 err_code_des  
	 * @param err_code_des err_code_des  
	 */
	public void setErr_code_des(String err_code_des) {
		this.err_code_des = err_code_des;
	}
	/**  
	 * 获取 partner_trade_no  
	 * @return partner_trade_no partner_trade_no  
	 */
	public String getPartner_trade_no() {
		return partner_trade_no;
	}
	/**  
	 * 设置 partner_trade_no  
	 * @param partner_trade_no partner_trade_no  
	 */
	public void setPartner_trade_no(String partner_trade_no) {
		this.partner_trade_no = partner_trade_no;
	}
	/**  
	 * 获取 mch_id  
	 * @return mch_id mch_id  
	 */
	public String getMch_id() {
		return mch_id;
	}
	/**  
	 * 设置 mch_id  
	 * @param mch_id mch_id  
	 */
	public void setMch_id(String mch_id) {
		this.mch_id = mch_id;
	}
	/**  
	 * 获取 detail_id  
	 * @return detail_id detail_id  
	 */
	public String getDetail_id() {
		return detail_id;
	}
	/**  
	 * 设置 detail_id  
	 * @param detail_id detail_id  
	 */
	public void setDetail_id(String detail_id) {
		this.detail_id = detail_id;
	}
	/**  
	 * 获取 status  
	 * @return status status  
	 */
	public String getStatus() {
		return status;
	}
	/**  
	 * 设置 status  
	 * @param status status  
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	/**  
	 * 获取 reason  
	 * @return reason reason  
	 */
	public String getReason() {
		return reason;
	}
	/**  
	 * 设置 reason  
	 * @param reason reason  
	 */
	public void setReason(String reason) {
		this.reason = reason;
	}
	/**  
	 * 获取 openid  
	 * @return openid openid  
	 */
	public String getOpenid() {
		return openid;
	}
	/**  
	 * 设置 openid  
	 * @param openid openid  
	 */
	public void setOpenid(String openid) {
		this.openid = openid;
	}
	/**  
	 * 获取 transfer_name  
	 * @return transfer_name transfer_name  
	 */
	public String getTransfer_name() {
		return transfer_name;
	}
	/**  
	 * 设置 transfer_name  
	 * @param transfer_name transfer_name  
	 */
	public void setTransfer_name(String transfer_name) {
		this.transfer_name = transfer_name;
	}
	/**  
	 * 获取 payment_amount  
	 * @return payment_amount payment_amount  
	 */
	public String getPayment_amount() {
		return payment_amount;
	}
	/**  
	 * 设置 payment_amount  
	 * @param payment_amount payment_amount  
	 */
	public void setPayment_amount(String payment_amount) {
		this.payment_amount = payment_amount;
	}
	/**  
	 * 获取 transfer_time  
	 * @return transfer_time transfer_time  
	 */
	public String getTransfer_time() {
		return transfer_time;
	}
	/**  
	 * 设置 transfer_time  
	 * @param transfer_time transfer_time  
	 */
	public void setTransfer_time(String transfer_time) {
		this.transfer_time = transfer_time;
	}
	/**  
	 * 获取 desc  
	 * @return desc desc  
	 */
	public String getDesc() {
		return desc;
	}
	/**  
	 * 设置 desc  
	 * @param desc desc  
	 */
	public void setDesc(String desc) {
		this.desc = desc;
	}
}
