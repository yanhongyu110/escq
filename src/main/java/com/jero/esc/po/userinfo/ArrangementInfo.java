package com.jero.esc.po.userinfo;

public class ArrangementInfo {
	private String arrId;
	private String arrSeverid;
	private String arrStarttime;
	private String arrEndtime;
	private String arrCurday;
	private String arrContent;
	private String arrComment;
	
	
	public String getArrId() {
		return arrId;
	}
	public void setArrId(String arrId) {
		this.arrId = arrId;
	}
	public String getArrSeverid() {
		return arrSeverid;
	}
	public void setArrSeverid(String arrSeverid) {
		this.arrSeverid = arrSeverid;
	}
	public String getArrStarttime() {
		return arrStarttime;
	}
	public void setArrStarttime(String arrStarttime) {
		this.arrStarttime = arrStarttime;
	}
	public String getArrEndtime() {
		return arrEndtime;
	}
	public void setArrEndtime(String arrEndtime) {
		this.arrEndtime = arrEndtime;
	}
	public String getArrCurday() {
		return arrCurday;
	}
	public void setArrCurday(String arrCurday) {
		this.arrCurday = arrCurday;
	}
	public String getArrContent() {
		return arrContent;
	}
	public void setArrContent(String arrContent) {
		this.arrContent = arrContent;
	}
	public String getArrComment() {
		return arrComment;
	}
	public void setArrComment(String arrComment) {
		this.arrComment = arrComment;
	}
	
	
	public String toString() {
		return "Arrange [arrId=" + arrId + ", arrSeverid=" + arrSeverid + ", arrStarttime=" + arrStarttime
				+ ", arrEndtime=" + arrEndtime + ", arrCurday=" + arrCurday + ", arrContent=" + arrContent
				+ ", arrComment=" + arrComment + "]";
	}
	
	
	
	
	
}
