public class VideoInfo {
	/** 主键id */
	private String videoId;
	/** 是否删除 0否 1是 */
	private String videoIsDel;
	/** 创建时间 */
	private Date videoCreateTime;
	/** 发布人id */
	private String logId;
	/** 发布状态 0审核通过1审核未通过2审核中 */
	private String videoStuta;
	/** 视频标题 */
	private String videoTitle;
	/** 视频地址 */
	private String videoUrl;
	public VideoInfo(String videoId, String videoIsDel, Date videoCreateTime, String logId, String videoStuta,
			String videoTitle, String videoUrl) {
		super();
		this.videoId = videoId;
		this.videoIsDel = videoIsDel;
		this.videoCreateTime = videoCreateTime;
		this.logId = logId;
		this.videoStuta = videoStuta;
		this.videoTitle = videoTitle;
		this.videoUrl = videoUrl;
	}
	public VideoInfo() {
		super();
	}
	/**  
	 * 获取 videoId  
	 * @return videoId videoId  
	 */
	public String getVideoId() {
		return videoId;
	}
	/**  
	 * 设置 videoId  
	 * @param videoId videoId  
	 */
	public void setVideoId(String videoId) {
		this.videoId = videoId;
	}
	/**  
	 * 获取 videoIsDel  
	 * @return videoIsDel videoIsDel  
	 */
	public String getVideoIsDel() {
		return videoIsDel;
	}
	/**  
	 * 设置 videoIsDel  
	 * @param videoIsDel videoIsDel  
	 */
	public void setVideoIsDel(String videoIsDel) {
		this.videoIsDel = videoIsDel;
	}
	/**  
	 * 获取 videoCreateTime  
	 * @return videoCreateTime videoCreateTime  
	 */
	public Date getVideoCreateTime() {
		return videoCreateTime;
	}
	/**  
	 * 设置 videoCreateTime  
	 * @param videoCreateTime videoCreateTime  
	 */
	public void setVideoCreateTime(Date videoCreateTime) {
		this.videoCreateTime = videoCreateTime;
	}
	/**  
	 * 获取 logId  
	 * @return logId logId  
	 */
	public String getLogId() {
		return logId;
	}
	/**  
	 * 设置 logId  
	 * @param logId logId  
	 */
	public void setLogId(String logId) {
		this.logId = logId;
	}
	/**  
	 * 获取 videoStuta  
	 * @return videoStuta videoStuta  
	 */
	public String getVideoStuta() {
		return videoStuta;
	}
	/**  
	 * 设置 videoStuta  
	 * @param videoStuta videoStuta  
	 */
	public void setVideoStuta(String videoStuta) {
		this.videoStuta = videoStuta;
	}
	/**  
	 * 获取 videoTitle  
	 * @return videoTitle videoTitle  
	 */
	public String getVideoTitle() {
		return videoTitle;
	}
	/**  
	 * 设置 videoTitle  
	 * @param videoTitle videoTitle  
	 */
	public void setVideoTitle(String videoTitle) {
		this.videoTitle = videoTitle;
	}
	/**  
	 * 获取 videoUrl  
	 * @return videoUrl videoUrl  
	 */
	public String getVideoUrl() {
		return videoUrl;
	}
	/**  
	 * 设置 videoUrl  
	 * @param videoUrl videoUrl  
	 */
	public void setVideoUrl(String videoUrl) {
		this.videoUrl = videoUrl;
	}
	@Override
	public String toString() {
		return "VideoInfo [videoId=" + videoId + ", videoIsDel=" + videoIsDel + ", videoCreateTime=" + videoCreateTime
				+ ", logId=" + logId + ", videoStuta=" + videoStuta + ", videoTitle=" + videoTitle + ", videoUrl=" + videoUrl
				+ "]";
	}
}
