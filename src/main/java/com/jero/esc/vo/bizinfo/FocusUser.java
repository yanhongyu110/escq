package com.jero.esc.vo.bizinfo;

public class FocusUser {
	
	private String futpUser;
    
    private String futpProvider;
    
    private String logCell;

    private String logEmail;

    private String logName;
    
    private String userWechat;

    private String userPhoto;

	public String getFutpUser() {
		return futpUser;
	}

	public void setFutpUser(String futpUser) {
		this.futpUser = futpUser;
	}

	public String getFutpProvider() {
		return futpProvider;
	}

	public void setFutpProvider(String futpProvider) {
		this.futpProvider = futpProvider;
	}

	public String getLogCell() {
		return logCell;
	}

	public void setLogCell(String logCell) {
		this.logCell = logCell;
	}

	public String getLogEmail() {
		return logEmail;
	}

	public void setLogEmail(String logEmail) {
		this.logEmail = logEmail;
	}

	public String getLogName() {
		return logName;
	}

	public void setLogName(String logName) {
		this.logName = logName;
	}

	public String getUserWechat() {
		return userWechat;
	}

	public void setUserWechat(String userWechat) {
		this.userWechat = userWechat;
	}

	public String getUserPhoto() {
		return userPhoto;
	}

	public void setUserPhoto(String userPhoto) {
		this.userPhoto = userPhoto;
	}
	

}
